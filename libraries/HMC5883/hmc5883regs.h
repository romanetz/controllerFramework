#ifndef HMC5883DEFS_H
#define HMC5883DEFS_H

#define HMC5883_I2C_ADDRESS 0x1E

#define HMC5883_CONFIG_A 0
#define HMC5883_CONFIG_B 1
#define HMC5883_MODE 2
#define HMC5883_DATA_XH 3
#define HMC5883_DATA_XL 4
#define HMC5883_DATA_ZH 5
#define HMC5883_DATA_ZL 6
#define HMC5883_DATA_YH 7
#define HMC5883_DATA_YL 8
#define HMC5883_STATUS 9
#define HMC5883_ID_A 10
#define HMC5883_ID_B 11
#define HMC5883_ID_C 12

#define HMC5883_DO2 (1 << 4)
#define HMC5883_DO1 (1 << 3)
#define HMC5883_DO0 (1 << 2)

enum HMC5883DataRate {
	HMC5883_DATARATE_0_75HZ = 0,
	HMC5883_DATARATE_1_5HZ = HMC5883_DO0,
	HMC5883_DATARATE_3HZ = HMC5883_DO1,
	HMC5883_DATARATE_7_5HZ = HMC5883_DO1 | HMC5883_DO0,
	HMC5883_DATARATE_15HZ = HMC5883_DO2,
	HMC5883_DATARATE_30_HZ = HMC5883_DO2 | HMC5883_DO0,
	HMC5883_DATARATE_75_HZ = HMC5883_DO2 | HMC5883_DO1
};

#define HMC5883_MS1 (1 << 1)
#define HMC5883_MS0 (1 << 0)

enum HMC5883MeasurementMode {
	HMC5883_MODE_NORMAL = 0,
	HMC5883_MODE_POS_BIAS = HMC5883_MS0,
	HMC5883_MODE_NEG_BIAS = HMC5883_MS1
};

#define HMC5883_GN2 (1 << 7)
#define HMC5883_GN1 (1 << 6)
#define HMC5883_GN0 (1 << 5)

enum HMC5883Gain {
	HMC5883_GAIN_0_9GA = 0, ///< 1280 LSB/Ga
	HMC5883_GAIN_1_2GA = HMC5883_GN0, ///< 1024 LSB/Ga
	HMC5883_GAIN_1_9GA = HMC5883_GN1, ///< 768 LSB/Ga
	HMC5883_GAIN_2_5GA = HMC5883_GN1 | HMC5883_GN0, ///< 614 LSB/Ga
	HMC5883_GAIN_4_GA = HMC5883_GN2, ///< 415 LSB/Ga
	HMC5883_GAIN_4_6GA = HMC5883_GN2 | HMC5883_GN0, ///< 361 LSB/Ga
	HMC5883_GAIN_5_5GA = HMC5883_GN2 | HMC5883_GN1, ///< 307 LSB/Ga
	HMC5883_GAIN_7_9GA = HMC5883_GN2 | HMC5883_GN1 | HMC5883_GN0 ///< 219 LSB/Ga
};

#define HMC5883_MD1 (1 << 1)
#define HMC5883_MD0 (1 << 0)

enum HMC5883Mode {
	HMC5883_MODE_CONTINUOS = 0,
	HMC5883_MODE_SINGLE = HMC5883_MD0,
	HMC5883_MODE_IDLE = HMC5883_MD1,
	HMC5883_MODE_SLEEP = HMC5883_MD1 | HMC5883_MD0
};

#define HMC5883_STATUS_REN (1 << 2)
#define HMC5883_STATUS_LOCK (1 << 1)
#define HMC5883_STATUS_RDY (1 << 0)

#define HMC5883_ID_A_VALUE 'H'
#define HMC5883_ID_B_VALUE '4'
#define HMC5883_ID_C_VALUE '3'

#endif // HMC5883DEFS_H
